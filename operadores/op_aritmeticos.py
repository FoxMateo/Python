#suma y resta (+ y -)
suma = 5 + 5
resta = 10 - 5

#multa y division (* y /)
multi = 12 * 6
division = 12 / 5 #devuelve un dato float siempre

#potenciacion (exponente) (**)
exponente = 12 ** 6

#division baja (//)
division_baja = 12 // 5 #devuelve un dato entero redondeado hacia abajo

#resto o modulo
resto = 12 % 5

print(suma)
print(resta)
print(multi)
print(division)
print(exponente)
print(division_baja)
print(resto)